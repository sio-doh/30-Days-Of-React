// 7. What are the files and folders inside a project folder(package.json, package-lock.json or yarn.lock, .gitignore,node_modules and public)?
// - .gitignore specifies which files and directories Git should ignore whilst tracking changes.
// - node_modules consists of all installed packages and their dependencies  needed for the project to run.
// - public includes static assets of the project like images and HTML files.
// - package-lock.json / yarn.lock lock down extract versions of installed packages to guarantee steady behaviour over different machines.
// - package.json holds information about dependencies, versions, scripts, metadata relating to the project.

