// Exercises: Level 1
// 1. What is a module?
// Modules are reusable UI elements that define a piece of a webpage or 
// application. They help organise the codebase into manageable portions, 
// encouraging code reusability through different pieces of the application.

// 2. What is package? 
// A package is a collection of related modules that may be installed and 
// maintained by a package manager like npm. Using packages saves developers 
// time by utilising pre-built solutions for common issues instead of building 
// everything from scratch.

// 3. What is the difference between a module and a package.
// A module is a single file or group of files that accomplish a certain 
// functionality. A package may be a collection of modules or 
// components that may be installed via npm. 

// 4. What is NPM?
// Node Package Manager is a tool for installing and maintaining modules. 

// 5. What is Webpack? 
// A module bundler for managing the build process of web apps.

